package com.neusoft.it.entity;

public class teacher {
	private int teaId;
	private String teaName;
	private String teaTel;
	private String teaInformation;
	private String teaPwd;
	public int getTeaId() {
		return teaId;
	}
	public void setTeaId(int teaId) {
		this.teaId = teaId;
	}
	public String getTeaName() {
		return teaName;
	}
	public void setTeaName(String teaName) {
		this.teaName = teaName;
	}
	public String getTeaTel() {
		return teaTel;
	}
	public void setTeaTel(String teaTel) {
		this.teaTel = teaTel;
	}
	public String getTeaInformation() {
		return teaInformation;
	}
	public void setTeaInformation(String teaInformation) {
		this.teaInformation = teaInformation;
	}
	public String getTeaPwd() {
		return teaPwd;
	}
	public void setTeaPwd(String teaPwd) {
		this.teaPwd = teaPwd;
	}
	

}
